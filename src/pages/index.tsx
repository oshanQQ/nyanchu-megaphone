import type { NextPage } from "next";
import Head from "next/head";
import { useState } from "react";

const Home: NextPage = () => {
  const [inputText, setInputText] = useState("");

  const assignOnChange = (event: any) => {
    setInputText(event.target.value);
  };

  const copyTextToClipboard = (text: string) => {
    navigator.clipboard.writeText(text).then(
      () => {
        console.log("'" + text + "'" + "was coppied!");
      },
      (err) => {
        console.log(err + ": Could not copy");
      }
    );
  };

  const convertText = (text: string) => {
    const isEmpty = text === "";
    return isEmpty ? "" : text.split("").join("゛") + "゛";
  };

  return (
    <div className="bg-nyanchu-yellow flex flex-col items-center">
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <div className="text-nyanchu-blue text-2xl font-bold my-6">
        {convertText("にゅうりょくするにゃ")}
      </div>
      <textarea
        className="border-2 border-solid bg-white border-nyanchu-blue rounded-3xl h-36 md:w-96 w-64 my-6 p-2 resize-none outline-none"
        value={inputText}
        onChange={assignOnChange}
      />
      <textarea
        className="border-2 border-solid bg-white border-nyanchu-blue rounded-3xl h-36 md:w-96 w-64 my-6 p-2 resize-none outline-none"
        defaultValue={convertText(inputText)}
        disabled
      />
      <button
        className="text-nyanchu-blue text-2xl font-bold my-6 cursor-pointer"
        onClick={() => copyTextToClipboard(convertText(inputText))}
      >
        {convertText("コピー")}
      </button>
    </div>
  );
};

export default Home;
